function changeColor() 
{
    if(document.getElementById("color").value ==
    "pink")
    document.getElementById("siteTheme").style.backgroundColor="pink";
    
    if(document.getElementById("color").value ==
    "dark")
    document.getElementById("siteTheme").style.backgroundColor="lightgray";

    if(document.getElementById("color").value ==
    "crazy")
    document.getElementById("siteTheme").style.backgroundColor="yellow";

}
